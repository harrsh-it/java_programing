public class variable {
    public static void main(String[] args) {
        
        int myNumber = 8; //initilization
        int yourNumber;  //Declaration
        yourNumber= 45;

        System.out.println(myNumber);
        System.out.println(yourNumber); //45

        yourNumber= 234;
        System.out.println(yourNumber); //234



        boolean isValid= true;
        System.out.println(isValid);




        float myFloat = 5;
        System.out.println(myFloat); //5.0

        // float myFloat2 = 5.0; //error

        float myFloat2 = 5.0f;        // use f or F
        System.out.println(myFloat2); //5.0




        
        double myDouble = 9;
        System.out.println(myDouble);//9.0

        double myDouble2=3.0;
        System.out.println(myDouble2); //3.0

        double myDouble3 = 3.3456765432345;
        System.out.println(myDouble3);




        String wishes ="Car";
        System.out.println(wishes);



        char myCharacter ='a';
        System.out.println(myCharacter); 
    }
}
